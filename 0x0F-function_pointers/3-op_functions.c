#include "3-calc.h"
/**
 * op_add - adds two numbers
 * @a: int
 * @b: int
 * Return: result of an add
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts two numbers
 * @a: int
 * @b: int
 * Return: result of a substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: int
 * @b: int
 * Return: result of a multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: int
 * @b: int
 * Return: result of a divide
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of a number
 * @a: int
 * @b: int
 * Return: result of a modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
